sub processar{
    my ($self, $consolidacao, $registroConsolidacao, $conteudo) = @_;

    my $logradouro = $conteudo->{formCadernoA}{enderecoImovel}{logradouro};

    for my $k (keys %translate) {
        if ($logradouro =~ /$k/is) {
            $logradouro = $translate{$k};
            last;
        }
    }

    $conteudo->{formCadernoA}{enderecoImovel}{logradouro} = $logradouro;
}


